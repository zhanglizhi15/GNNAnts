class GAT(nn.Module):
    def __init__(self,
                 in_feats,
                 n_hidden,
                 n_classes,
                 n_layers,
                 num_heads,
                 activation,
                 dropout=0.):
        super().__init__()
        self.n_layers = n_layers
        self.n_hidden = n_hidden
        self.n_classes = n_classes
        self.layers = nn.ModuleList()
        self.num_heads = num_heads
        self.dropout = nn.Dropout(dropout)
        self.activation = activation
        

        if self.n_layers==1:  #should cache the features
            self.layers.append(GATConvCacheReuse(in_feats,n_classes,num_heads=num_heads,feat_drop=dropout,attn_drop=dropout,activation=activation,negative_slope=0.2))
        elif self.n_layers==2:
            self.layers.append(GATConvCacheReuse(in_feats,n_hidden,num_heads=num_heads,feat_drop=dropout,attn_drop=dropout,activation=activation,negative_slope=0.2))
                #self.bns.append(nn.BatchNorm1d(n_hidden))
            self.layers.append(dglnn.GATConv(n_hidden * num_heads,n_classes,num_heads=num_heads,feat_drop=dropout,attn_drop=dropout,activation=None,negative_slope=0.2))
        else:
            self.layers.append(GATConv(in_feats,n_hidden,num_heads=num_heads,feat_drop=dropout,attn_drop=dropout,activation=activation,negative_slope=0.2))
            for i in range(1,n_layers - 2):
                self.layers.append(GATConv(n_hidden* num_heads,n_hidden,num_heads=num_heads,feat_drop=dropout,attn_drop=dropout,activation=activation,negative_slope=0.2))
                #self.bns.append(nn.BatchNorm1d(n_hidden))
            self.layers.append(GATConvCacheReuse(n_hidden* num_heads,n_hidden,num_heads=num_heads,feat_drop=dropout,attn_drop=dropout,activation=activation,negative_slope=0.2))
            self.layers.append(dglnn.GATConv(n_hidden * num_heads,n_classes,num_heads=num_heads,feat_drop=dropout,attn_drop=dropout,activation=None,negative_slope=0.2))

    def forward(self, blocks_and_repeat, input_reuse):
        blocks = blocks_and_repeat[0]
        prev_layer_repeat = blocks_and_repeat[1]
        step = blocks_and_repeat[2]
        x=input_reuse[0]
        reuse_embedding = input_reuse[1]
        for l, (layer, block) in enumerate(zip(self.layers, blocks)):
            if (l == self.n_layers-2):  
                x, cache_embedding = layer(block, x, prev_layer_repeat, step, reuse_embedding)
            else:
                x = layer(block, x)
            #x = layer(block, x)
            if l < self.n_layers - 1:
                x = x.flatten(1)
                #h = self.dropout(h)
        x = x.mean(1)
        return x.log_softmax(dim=-1), cache_embedding